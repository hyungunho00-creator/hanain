               '건강기능식품 원료로 등록된 것은 "감태추출물"이 대표적이며, 미역은 식약처 일반식품·식품 영양으로 더 자주 분류됩니다.\n'
               '\n'
               '## 4. 용도와 안전성\n'
               '\n'
               '**미역:**\n'
               '- 일상 식이 (미역국, 무침, 쌈)\n'
               '- 요오드 과잉 주의 (특히 임산부·갑상선 환자)\n'
               '- 비타민 K 풍부 → 항응고제 복용자 주의\n'
               '\n'
               '**감태(추출물):**\n'
               '- 건강기능식품 원료 (식약처 등록)\n'
               '- 일상 식이보다 추출물 형태가 일반적\n'
               '- 안전성 자료는 식약처 인정 자료가 정리되어 있음\n'
