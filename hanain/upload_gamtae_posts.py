def save_post(payload):
    r = requests.post(f"{SUPABASE_URL}/rest/v1/posts", headers=SB_HEADERS, json=payload)
    if r.status_code in (200, 201):
        print(f"  ✅ 저장: {payload['slug']}")
        return True
    print(f"  ❌ 실패 {r.status_code}: {r.text[:300]}")
    return False


def main():
    print("🌊 감태추출물 카테고리별 8개 포스팅 업로드 시작\n")

    # 기존 슬러그 확인
    r = requests.get(f"{SUPABASE_URL}/rest/v1/posts?select=slug&limit=500", headers=SB_HEADERS)
    existing = {p["slug"] for p in r.json()} if r.status_code == 200 else set()
    print(f"기존 포스팅 {len(existing)}개 확인\n")

    success = 0
    for i, post in enumerate(POSTS, 1):
        print(f"[{i}/8] [{post['category']}] {post['title'][:45]}...")

        slug = post["slug"]
        if slug in existing:
            print(f"  ⚠️ 이미 존재 — 건너뜀")
            continue

        now = datetime.now(timezone.utc).isoformat()
        payload = {
            "title":        post["title"],
            "slug":         slug,
            "excerpt":      post["excerpt"],
            "meta_title":   post["meta_title"],
            "meta_desc":    post["meta_desc"],
            "tags":         post["tags"],
            "content":      post["content"],
            "category":     post["category"],
            "status":       "published",
            "published_at": now,
            "og_image":     "/og-image.png",
            "created_at":   now,
        }

        content_len = len(post["content"])
        print(f"  📏 본문 {content_len:,}자 | 태그: {', '.join(post['tags'][:4])}")

        if save_post(payload):
            existing.add(slug)
            success += 1

    print(f"\n🎉 완료! {success}/{len(POSTS)}개 업로드 성공")


if __name__ == "__main__":
    main()
