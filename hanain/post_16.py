def make_slug(title):
    import re, unicodedata
    # 간단한 slug: 영문+숫자만 남기고 하이픈 연결
    s = unicodedata.normalize('NFKD', title)
    s = s.encode('ascii', 'ignore').decode('ascii')
    s = re.sub(r'[^\w\s-]', '', s.lower())
    s = re.sub(r'[\s_]+', '-', s)
    s = re.sub(r'-+', '-', s).strip('-')
    if len(s) < 10:  # 한글만 있어서 빈 slug가 된 경우
        words = ['phlorotannin', 'health', 'guide', 'korea']
        s = '-'.join(words[:3])
    return s[:75]


