def main():
    parser = argparse.ArgumentParser(description="Gemini 자동 블로그 포스팅 v4")
    parser.add_argument("--count",    type=int, default=3)
    parser.add_argument("--topic",    type=str, default="")
    parser.add_argument("--keywords", type=str, default="")
    parser.add_argument("--category", type=str, default="general")
    parser.add_argument("--dry-run",  action="store_true")
    args = parser.parse_args()

    print(f"\n{'='*60}")
    print(f"  🌊 플로로탄닌 Gemini 자동 포스팅 v4")
    print(f"  내장 주제 풀: {len(BUILTIN_TOPICS)}개")
    print(f"  모드: {'DRY RUN' if args.dry_run else '실제 발행'} | 목표: {args.count}개")
    print(f"{'='*60}")

    existing = get_existing_slugs()
    print(f"기존 발행 글: {len(existing)}개")

    # ── 직접 주제 입력 ──
    if args.topic:
        kws = [k.strip() for k in args.keywords.split(",") if k.strip()] if args.keywords else []
        create_post(args.topic, kws, args.category, args.dry_run)
        return

    # ── DB 주제 우선, 없으면 내장 풀 ──
    db_topics = fetch_topics_from_db(args.count * 3)

    available = list(BUILTIN_TOPICS)
    random.shuffle(available)

    posted = 0
    db_used = 0

    # 1) DB 주제
    for row in db_topics:
        if posted >= args.count:
            break
        success = create_post(row["topic"], row.get("keywords") or [], row.get("category", "general"), args.dry_run)
        if success:
            posted += 1
            db_used += 1
            if not args.dry_run:
                mark_topic_used(row["id"])
        time.sleep(10)

    # 2) 내장 풀 보충
    if posted < args.count:
        print(f"\n  📦 내장 풀에서 {args.count - posted}개 추가")
        for (category, topic, keywords) in available:
            if posted >= args.count:
                break
            slug_hint = re.sub(r"[^a-z0-9]", "-", topic[:30].lower())
            if any(slug_hint[:12] in s for s in existing):
                continue
            success = create_post(topic, keywords, category, args.dry_run)
            if success:
                posted += 1
                existing.add(slug_hint)
            time.sleep(10)

    print(f"\n{'='*60}")
    print(f"  ✅ 완료: {posted}/{args.count}개 발행")
    if db_used:
        print(f"     (DB: {db_used}개 / 내장 풀: {posted-db_used}개)")
    print(f"  🌐 블로그: {SITE_URL}/blog")
    print(f"{'='*60}\n")

if __name__ == "__main__":
    main()
